#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char *arr;

	arr = ft_itoa(n);
	if (n <= (-2147483647) - 1)
		arr = "-2147483648";
	ft_putstr_fd(arr, fd);
}
